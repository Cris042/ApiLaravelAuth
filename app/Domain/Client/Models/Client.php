    protected $table = 'client';

    protected $fillable = [
        'name',
        'cpf',
        'telefone'
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'created_at', 'updated_at',
    ];
}
